const today = new Date();

console.log("Year is: ", today.getFullYear());
console.log("Month is: ", today.getMonth());
console.log("Day is: ", today.getDay());
console.log("Date is: ", today.getDate());
console.log("Time is: ", today.getTime());
console.log("Hours are: ", today.getHours());
console.log("Minutes are: ", today.getMinutes());
console.log("Seconds are: ", today.getSeconds());
console.log("Date string is: ", today.toDateString());
console.log("Time string is: ", today.toTimeString());